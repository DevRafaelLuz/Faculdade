/* Elabore um algoritmo que receba três notas de um aluno e retorne a sua
    média aritmética. Exemplo: nota1 = 10.0, nota2 = 5.5, nota3 = 8.0. Média: 7.83 */

package Exercicios01;

public class Exercicio15 {
    public static void main(String[] args) {
        double nota1 = 10.0;
        double nota2 = 5.5;
        double nota3 = 8.0;
        double media = (nota1 + nota2 + nota3) / 3;
        
        System.out.println("Média: " + media);    
    }
}
